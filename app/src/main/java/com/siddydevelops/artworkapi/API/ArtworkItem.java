package com.siddydevelops.artworkapi.API;

public class ArtworkItem {
    private int id;
    private String title;
    private String artist_title;
    private String date_start;
    private String provenance_text;

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getArtist_title() {
        return artist_title;
    }

    public String getDate_start() {
        return date_start;
    }

    public String getProvenance_text() {
        return provenance_text;
    }
}
